package com.tecnolog.autocoleta.salvarcoleta.payload;

public class SalvaColetaDimensoesModel {
    private Integer Qtd;
    private Double Comp;
    private Double Larg;
    private Double Alt;
    private Double Kg;

    public Integer getQtd() { return Qtd; }
    public void setQtd(Integer qtd) { Qtd = qtd; }

    public Double getComp() { return Comp; }
    public void setComp(Double comp) { Comp = comp; }

    public Double getLarg() { return Larg; }
    public void setLarg(Double larg) { Larg = larg; }

    public Double getAlt() { return Alt; }
    public void setAlt(Double alt) { Alt = alt; }

    public Double getKg() { return Kg; }
    public void setKg(Double kg) { Kg = kg; }
}
